export type Language = 'pt' | 'fil' | 'vi';

export const i18n = (language: Language = 'pt') => {
  return {
    t: (key: string): string => {
      const keys = key.split('.');
      let value: any = translations[language];
      
      for (const k of keys) {
        value = value?.[k];
      }
      
      return typeof value === 'string' ? value : key;
    },
    language
  };
};

export default translations;
